 * Pagination logic for contacts table:
 * - Connect to database
 * - Get current page number
 * - Calculate total rows in table 
 * - Calculate number of pages
 * - Offset to fetch rows for current page
 * - Fetch contacts for current page
 * - Close database connection
 */

// Connect to database
global $conn;
include 'conn.php';

// Get current page number
$page = isset($_GET['page']) ? intval($_GET['page']) : 1;

// Total number of rows
$sql = "SELECT COUNT(*) FROM contacts";
$result = $conn->query($sql);
$total_rows = $result->fetch_row()[0];

if ($page > 2) {
    $minus = $page - 2;
} else {
    $minus = 1;
}

// Number of rows per page
$per_page = 20;

// Calculate total number of pages
$total_pages = ceil($total_rows / $per_page);

// Offset for current page
$offset = ($page - 1) * $per_page;

// Fetch contacts for the current page
$sql = "SELECT * FROM contacts LIMIT $offset, $per_page";
$result = $conn->query($sql);
$contacts = $result->fetch_all(MYSQLI_ASSOC);

// Close connection
$conn->close();


